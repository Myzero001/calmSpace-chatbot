def build_vectorstore(kb: Dict[str, List[str]]) -> FAISS:
    texts: List[str] = []
    for topic, msgs in kb.items():
        for msg in msgs:
            texts.append(f"[{topic}] {msg}")

    embedding_model = HuggingFaceEmbeddings(model_name="thenlper/gte-small")
    print("‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á Vector Store ‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡πÉ‡∏´‡πâ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÉ‡∏à...")
    db = FAISS.from_texts(texts, embedding_model) # ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏ó‡∏µ‡πà‡∏Ñ‡∏•‡πâ‡∏≤‡∏¢‡∏Å‡∏±‡∏ô
    return db


